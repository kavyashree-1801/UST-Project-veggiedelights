    <h1>Veg Manchurian</h1>
    <img src="https://media.istockphoto.com/id/1334115358/photo/cabbage-manchurian.jpg?s=612x612&w=0&k=20&c=lZvW1lWr03mQszDbx4v59IAnxWacQ_Ti275hjj18hcE=" alt="Veg Manchurian">
    <p>Chinese-style vegetable balls cooked in spicy soy-based sauce, perfect with rice or noodles.</p>

    <h2>Ingredients</h2>
    <ul>
      <li>1 cup grated cabbage, carrot, and capsicum</li>
      <li>2 tbsp cornflour</li>
      <li>2 tbsp all-purpose flour</li>
      <li>Salt and pepper to taste</li>
      <li>Oil for frying</li>
      <li>For sauce: 2 tbsp soy sauce, 1 tbsp tomato ketchup, 1 tsp chili sauce, 1 cup water, 1 tsp cornflour mixed in water</li>
    </ul>

    <h2>Instructions</h2>
    <ol>
      <li>Mix vegetables, cornflour, flour, salt, and pepper. Shape into balls.</li>
      <li>Deep fry until golden brown and crisp.</li>
      <li>In a pan, heat oil, add sauces and cornflour slurry, cook until thick.</li>
      <li>Add fried balls to the sauce, toss well.</li>
      <li>Serve hot with steamed rice or noodles.</li>
    </ol>

